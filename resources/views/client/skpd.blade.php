<h3>MAL PELAYANAN PUBLIK</h3>
<h5 class="text-muted">KABUATEN BANYUWANGI</h5>
<br>
<p> 
    Peningkatan kualitas pelayanan publik menjadi tuntutan yang selalu disuarakan oleh
    berbagai kalangan, baik itu kalanagn dunia usaha mupun masyarakat. Pelayanan yang
    dapat memuaskan dan memenuhi harapan pencari layanan menjadi alasan rasional yang
    selalu dikedepankan.
</p>

<p>
    Untuk memenuhi harapan masyarakat tersebut, Pemerintah Kabupaten Banyuwangi
    meluncurkan Mal Pelayanan Publik, Jumat (6/10/2017). Ini merupakan tempat pelayanan
    erpadu di mana semua pengurusan dokumen dilakukan di satu tempat tersebut.
</p>

<p>
    Pada saat ini, Mal Pelayanan Publik telah melayani 199 jenis pelayanan kepada 
    masyarakat dalam satu atap, dimana terdapat 11 SKPD pemerintah Daerah  dan 12 BUMN/K/L. 
    Mulai administrasi kependudukan seperti Akte Kelahiran, Kematian, Kartu Identitas Anak, 
    KTP; beragam jenis izin usaha, BPJS Kesehatan, BPJS Ketenagakerjaan, PDAM, pelayanan izin 
    terkait kendaraan,perpanjangan SIM,hingga pembayaran retribusi daerah, nikah, haji, 
    pelayanan pertanahan dan sebagainya.
</p>

<p>
    Dengan Ikhtiar mewujudkan pelayanan yang lebih baik. Dengan tekad serta gotong-royong 
    warga dan birokrasi. Warga tidak perlu ke berbagai instansi untuk mengurus satu keperluan, 
    semuanya cukup di Mal Pelayanan Publik. Dengan mal pelayanan publik, pelayanan menjadi 
    ringkas dan transparan. "Tidak ada pungutan tidak resmi. Kalau ada yang mewajibkan perlu 
    retribusi daerah, bisa langsung bayar ke loket bank yang disediakan.
</p>
